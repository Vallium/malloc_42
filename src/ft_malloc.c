#include <libft_malloc.h>
#include <sys/mman.h>

void	*malloc(size_t size)
{
	void	*ret;

	ret = mmap(0, size, PROT_READ | PROT_WRITE, MAP_ANON | MAP_PRIVATE, -1, 0);
	return (ret);
}
